package day07_ifstatement;

public class if_else {
    public static void main(String[] args) {
        int num=0;
        if (num>=0) {
            System.out.println(num + " is positive");
        }
        if (num<0){

            System.out.println(num+ " is negative");
        }
        else {
            System.out.println(num + " is positive");
        }

    }


}
