package day02_Variables;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("\tkanat");
        System.out.println("\t\t\t\t\tKanat");
        System.out.println("\nKanat \n adeka\n");
        System.out.println("adeka\nzuzu\nkakosha\n");
        System.out.println("My favorite TV show is \"Game Throne\"");
        // \" means favorite
        System.out.println("\\");
        System.out.println("/");
        System.out.println("my favorite book is 'java'");
        System.out.println("my favorite book is \'java\'");
    }
}
