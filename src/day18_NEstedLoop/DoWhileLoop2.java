package day18_NEstedLoop;

public class DoWhileLoop2 {
    public static void main(String[] args) {
        int num=105;

        while (num<104){
            System.out.print(num+" ");
            num++;
        }

        System.out.println("\n=========================");
        int num2=105;
        do{
            System.out.print(num2+" ");
            num2++;
        }while(num2<104);



    }
}
